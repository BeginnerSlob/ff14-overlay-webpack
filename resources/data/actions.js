class Action {
  constructor(id, level) {
    const action = actions[id] || new Array(7).fill("");
    this.ID = id;
    this.Name = actionChinese[id] ?? "unknown";
    this.Url = (() => {
      const u = action[rule.Url];
      if (u === "") return "000000/000405";
      if (u.length > 6) {
        return "0".repeat(13 - u.length) + u;
      } else if (u.length === 6) {
        return "000000/" + u;
      } else {
        return "000000/" + "0".repeat(6 - u.length) + u;
      }
    })();
    this.ActionCategory = action[rule.ActionCategory];
    this.ClassJobLevel = action[rule.ClassJobLevel];
    this.IsRoleAction = this.ClassJobLevel === 0 ? "FALSE" : action[rule.IsRoleAction];
    this.Recast100ms = getTrait(this.ID, "Recast100ms", level);
    this.MaxCharges = getTrait(this.ID, "MaxCharges", level);
    this.ClassJobCategory = this.ClassJobLevel === 0 ? [] : action[rule.ClassJobCategory].split(" ");
    this.Useful = level >= this.ClassJobLevel;
  }
}
